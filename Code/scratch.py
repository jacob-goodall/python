def count_to_3():
    for x in range(3):
        print(x)


def count_to_3mst():
    for y in range(4):
        if y == 3:
            break
        print(y)
    else:
        print('Finally Finished')


def count_to_10():
    for z in range(11):
        print(z)


def count_to_10_minus_5():
    for a in range(5, 11):
        print(a)


def count_even():
    for b in range(0, 11, 2):
        print(b)
        


